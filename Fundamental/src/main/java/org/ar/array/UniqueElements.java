package org.ar.array;
public class UniqueElements {
	public static void main(String[] args) {
        int ar[]=new int[] {1,3,7,1,7,2,5};
        for(int i=0;i<ar.length;i++) {
        	int key=ar[i];
        	int count=0;
        	for(int j=0;j<ar.length;j++) {
        		if(key==ar[j]) {
        			count++;
        		}
        			
        			
        	}//inner loop
        	if(count==1)
        		System.out.println(ar[i]);
        }
        // 1 7 1
        // 1
        //3 2 5
        
	}
}
